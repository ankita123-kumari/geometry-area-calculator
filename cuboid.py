length=int(input("enter length of cuboid  "))

breadth=int(input("enter breadth of cuboid  "))

height=int(input("enter height of cuboid  "))

surface_area= 2*(length*breadth + breadth*height + length*height)

volume= length*breadth*height

print(f"surface area of cuboid  {surface_area}")

print(f"volume of cuboid {volume}")
